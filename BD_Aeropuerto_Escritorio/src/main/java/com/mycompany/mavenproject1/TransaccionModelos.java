    private void btnEjecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEjecutarActionPerformed
        // TODO add your handling code here:
        if (modeloActual.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Primero busca un modelo");
            return;
        }
        
        String capacidadText = txtCapacidadNueva.getText().trim();
        
        if (capacidadText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingresa la nueva capacidad");
            txtCapacidadNueva.requestFocus();
            return;
        }
        
        try {
            int nuevaCapacidad = Integer.parseInt(capacidadText);
            
            if (nuevaCapacidad < 1 || nuevaCapacidad > 1000) {
                JOptionPane.showMessageDialog(this, "Capacidad debe ser 1-1000");
                txtCapacidadNueva.requestFocus();
                return;
            }
            
            int respuesta = JOptionPane.showConfirmDialog(this,
                "¿Cambiar capacidad de " + modeloActual + "?\n" +
                "De " + capacidadActual + " a " + nuevaCapacidad,
                "Confirmar",
                JOptionPane.YES_NO_OPTION);
            
            if (respuesta != JOptionPane.YES_OPTION) {
                return;
            }
            
            Connection conn = Conexion_DB.getInstance().getConnection();
            CallableStatement stmt = null;
            ResultSet rs = null;
            
            try {
                String sql = "{call SP_CAMBIO_CAPACIDAD(?, ?)}";
                stmt = conn.prepareCall(sql);
                stmt.setString(1, modeloActual);
                stmt.setInt(2, nuevaCapacidad);
                
                rs = stmt.executeQuery();
                
                if (rs.next()) {
                    String modelo = rs.getString("MODELO");
                    int antes = rs.getInt("ANTES");
                    int despues = rs.getInt("DESPUES");
                    double peso = rs.getDouble("PESO");
                    double densidad = rs.getDouble("KG_POR_PASAJERO");
                    
                    jTextArea1.setText(
                        "TRANSACCIÓN EXITOSA\n" +
                        "Modelo: " + modelo + "\n" +
                        "Capacidad anterior: " + antes + "\n" +
                        "Capacidad nueva: " + despues + "\n" +
                        "Peso: " + peso + "\n" +
                        "Densidad: " + String.format("%.2f", densidad) + " kg/pax"
                    );
                    
                    capacidadActual = despues;
                    
                    JOptionPane.showMessageDialog(this, "Cambio realizado exitosamente");
                    
                    txtCapacidadNueva.setText("");
                    txtCapacidadNueva.requestFocus();
                    
                } else {
                    JOptionPane.showMessageDialog(this, "No se obtuvieron resultados");
                }
                
            } catch (SQLException e) {
                manejarErrorSQL(e);
            } finally {
                try { if (rs != null) rs.close(); } catch (Exception e) {}
                try { if (stmt != null) stmt.close(); } catch (Exception e) {}
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "La capacidad debe ser un número");
            txtCapacidadNueva.requestFocus();
        }
    }//GEN-LAST:event_btnEjecutarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
         String modelo = txtBuscarNumeroModelo.getText().trim();
        
        if (modelo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingresa número de modelo");
            txtBuscarNumeroModelo.requestFocus();
            return;
        }
        
        Connection conn = null;
        PreparedStatement stmt = null;
        ResultSet rs = null;
        
        try {
            conn = Conexion_DB.getInstance().getConnection();
            
            String sql = "SELECT ModelNumber, Capacidad, Peso FROM ModelosAvion WHERE ModelNumber = ?";
            stmt = conn.prepareStatement(sql);
            stmt.setString(1, modelo);
            rs = stmt.executeQuery();
            
            if (rs.next()) {
                modeloActual = rs.getString("ModelNumber");
                capacidadActual = rs.getInt("Capacidad");
                pesoActual = rs.getDouble("Peso");
                
                jTextArea1.setText(
                    "Modelo: " + modeloActual + "\n" +
                    "Capacidad actual: " + capacidadActual + "\n" +
                    "Peso: " + pesoActual + "\n\n" +
                    "Ingresa nueva capacidad:"
                );
                
                txtCapacidadNueva.setEnabled(true);
                txtCapacidadNueva.requestFocus();
                txtCapacidadNueva.setText("");
                
            } else {
                jTextArea1.setText("Modelo no encontrado: " + modelo);
                resetearDatos();
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            resetearDatos();
        } finally {
            try { if (rs != null) rs.close(); } catch (Exception e) {}
            try { if (stmt != null) stmt.close(); } catch (Exception e) {}
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

