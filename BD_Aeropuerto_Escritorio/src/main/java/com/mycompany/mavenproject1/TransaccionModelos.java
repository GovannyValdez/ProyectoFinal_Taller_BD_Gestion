    private void manejarErrorSQL(SQLException e) {
        String error = e.getMessage();
        
        if (error.contains("Capacidad inválida")) {
            JOptionPane.showMessageDialog(this, "Error: Capacidad fuera de rango (1-1000)");
        } else if (error.contains("no existe")) {
            JOptionPane.showMessageDialog(this, "Error: Modelo no encontrado");
        } else {
            JOptionPane.showMessageDialog(this, "Error SQL: " + error);
        }
        
        jTextArea1.setText("TRANSACCIÓN FALLIDA\nError: " + error);
        resetearDatos();
    }
    
