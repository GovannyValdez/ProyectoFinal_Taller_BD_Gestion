    private void manejarErrorSQL(SQLException e) {
        String mensajeError = e.getMessage().toLowerCase();
        
        if (mensajeError.contains("fn_calcular_eficiencia") || 
            mensajeError.contains("function") || 
            mensajeError.contains("does not exist") ||
            mensajeError.contains("no existe")) {
            
            javax.swing.JOptionPane.showMessageDialog(this,
                "‚ùå La funci√≥n 'FN_CALCULAR_EFICIENCIA' no existe en la base de datos.\n\n" +
                "Por favor, ejecuta este SQL primero en DB2:\n\n" +
                "CREATE OR REPLACE FUNCTION FN_CALCULAR_EFICIENCIA (p_modelo VARCHAR(20)) " +
                "RETURNS DECIMAL(10,2) LANGUAGE SQL DETERMINISTIC NO EXTERNAL ACTION " +
                "RETURN (SELECT CAST(Capacidad * 100000.00 / Peso AS DECIMAL(10,2)) " +
                "FROM ModelosAvion WHERE ModelNumber = p_modelo)",
                "Funci√≥n no encontrada",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                "‚ùå Error de base de datos: " + e.getMessage(),
                "Error SQL",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        logger.severe("Error SQL al ejecutar funci√≥n: " + e.toString());
    }
    
    private void cerrarRecursos(ResultSet rs, PreparedStatement pstmt) {
        try {
            if (rs != null) rs.close();
            if (pstmt != null) pstmt.close();
        } catch (SQLException e) {
            logger.warning("Error al cerrar recursos: " + e.toString());
        }
    }
    
    
    
