    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        Connection conn = null;
        PreparedStatement pstmt = null;
        ResultSet rs = null;
        
        try {
            conn = Conexion_DB.getInstance().getConnection();
            
            if (conn == null) {
                mostrarErrorConexion();
                return;
            }
            
            String modelo = txtModelo.getText().trim();
            
            if (modelo.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "‚ùå Por favor ingrese un c√≥digo de modelo",
                    "Campo vac√≠o",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            String sql = "VALUES FN_CALCULAR_EFICIENCIA(?)";
            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, modelo);
            
            rs = pstmt.executeQuery();
            
            if (rs.next()) {
                double resultado = rs.getDouble(1);
                
                if (rs.wasNull()) {
                    txtResultado.setText("");
                    javax.swing.JOptionPane.showMessageDialog(this,
                        "‚ùå Modelo '" + modelo + "' no encontrado en la base de datos",
                        "Modelo no existe",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
                } else {
                    txtResultado.setText(String.format("%.2f", resultado));
                    jLabel5.setText(String.format("(Pasajeros por %.0f toneladas)", resultado >= 1000 ? resultado/1000 : resultado));
                    
                    String interpretacion = interpretarEficiencia(resultado);
                    mostrarInterpretacion(interpretacion);
                }
            } else {
                txtResultado.setText("");
                javax.swing.JOptionPane.showMessageDialog(this,
                    "‚ùå No se pudo calcular la eficiencia",
                    "Error en c√°lculo",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (SQLException e) {
            manejarErrorSQL(e);
        } finally {
            cerrarRecursos(rs, pstmt);
        }
    }
    
    private String interpretarEficiencia(double eficiencia) {
        if (eficiencia > 50) {
            return "‚≠ê Excelente eficiencia (avi√≥n muy eficiente)";
        } else if (eficiencia > 30) {
            return "üü¢ Buena eficiencia (avi√≥n eficiente)";
        } else if (eficiencia > 15) {
            return "üü° Eficiencia moderada (promedio)";
        } else if (eficiencia > 5) {
            return "üü† Baja eficiencia (avi√≥n pesado)";
        } else {
            return "üî¥ Muy baja eficiencia (avi√≥n muy pesado)";
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

